# --------------------------------------------------------------------------------------------------
# ---------------------------------- PRE-PARSING :: Sequence Node ----------------------------------
# --------------------------------------------------------------------------------------------------
from .. Visitors.Visitor import Visitor
from .. Lexer.Token      import Token

from .  Expression  import Expression
from .  Node        import Node

from typing import TypeVar
R = TypeVar('R')


# --------------------------------------------------------------------------------------------------
# ------------------------------------- CLASS :: Sequence Node -------------------------------------
# --------------------------------------------------------------------------------------------------
class Sequence(Expression):

    # ------------------------------------------------------------------------------------------
    # -------------------------------- ATTRIBUTES :: Attributes --------------------------------
    # ------------------------------------------------------------------------------------------
    elements: tuple[Node, ...]


    # ------------------------------------------------------------------------------------------
    # ------------------------------ CONSTRUCTION :: Construction ------------------------------
    # ------------------------------------------------------------------------------------------
    def __init__(self, *elements: Node) -> None:
        self.elements = elements


    # ------------------------------------------------------------------------------------------
    # --------------------------- STRINGIFICATION :: Stringification ---------------------------
    # ------------------------------------------------------------------------------------------
    def stringify(self) -> str:

        elements = ', '.join(f"'{e.__class__.__name__}'" for e in self.elements)
        return f"Sequence({elements})"

    def __repr__(self) -> str:
        return self.stringify()

    def __str__(self) -> str:
        return self.stringify()


    # ------------------------------------------------------------------------------------------
    # ------------------------------ VISITATION :: Accept Visitor ------------------------------
    # ------------------------------------------------------------------------------------------
    def accept(self, visitor: Visitor[R]) -> R:
        return visitor.visit_sequence(self)


    # ------------------------------------------------------------------------------------------
    # ------------------------------ PROPERTIES :: Bounds of Node ------------------------------
    # ------------------------------------------------------------------------------------------
    @property
    def start(self) -> Token:
        return self.expressions[0].start

    @property
    def end(self) -> Token:
        return self.expressions[-1].end